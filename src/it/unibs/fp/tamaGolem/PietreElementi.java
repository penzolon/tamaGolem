package it.unibs.fp.tamaGolem;
public class PietreElementi {
    private String tipoPietra;
    private int potenza;
    
    public PietreElementi(String tipoPietra, int potenza) {
        this.tipoPietra = tipoPietra;
        this.potenza = potenza;
    }

    public String getTipoPietra() {
        return tipoPietra;
    }

    public int getPotenza() {
        return potenza;
    }
        
    
}
